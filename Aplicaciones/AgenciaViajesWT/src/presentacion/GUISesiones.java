    private void llenarTabla(ArrayList<Sesion> lista_sesiones)
    {
        this.inicializarTabla();
        DefaultTableModel model = (DefaultTableModel) jtblSesiones.getModel();

        Object rowData[] = new Object[4];
        for(Sesion sesion : lista_sesiones) {
            rowData[0] = sesion.getUsuario();
            rowData[1] = sesion.getContrasenia();
            rowData[2] = sesion.getFecha_hora();
            rowData[3] = sesion.getIngreso_valido();
            model.addRow(rowData);
        }
    }
