    private void jbtnConsulrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnConsulrarActionPerformed
        String usuario_name = jtxtUsuario.getText().trim();
        if(usuario_name.equals(""))
        {
            Utilidades.mensajeAdvertencia("Debe ingresar un nombre de Usuario", "Advertencia");
            jtxtUsuario.requestFocus();
        }
        ArrayList<Sesion> lista_sesiones = gestor_sesiones.obtener_sesiones_por_usuario(usuario_name);
        
        if(lista_sesiones.isEmpty())
        {
            Utilidades.mensajeError("Este usuario no tiene registros o no existe.", "Informe");
            jtxtUsuario.setText("");
        }
        else
        {
            llenarTabla(lista_sesiones);
            jtxtUsuario.setText("");
        }
        
    }//GEN-LAST:event_jbtnConsulrarActionPerformed

